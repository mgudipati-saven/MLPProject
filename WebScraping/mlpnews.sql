            accretion expense. Adjusted EBITDA is commonly used as a
            supplemental financial measure by our management and external
            users of our financial statements, such as investors, commercial
            banks, research analysts and rating agencies, to assess: (1) the
            financial performance of our assets without regard to financing
            methods, capital structures or historical cost basis; (2) the
            ability of our assets to generate cash sufficient to pay interest
            and support our indebtedness; and (3) the viability of projects
            and the overall rates of return on alternative investment
            opportunities. Since Adjusted EBITDA excludes some, but not all,
            items that affect net income or loss and because these measures
            may vary among other companies, the Adjusted EBITDA data presented
            in this press release may not be comparable to similarly titled
            measures of other companies. The GAAP measure most directly
            comparable to Adjusted EBITDA is net cash flows provided by
