          total or by individual segment) is an important performance measure
          of the core profitability of our operations. This measure forms the
          basis of our internal financial reporting and is used by our
          management in deciding how to allocate capital resources among
          business segments. We believe that investors benefit from having
          access to the same financial measures that our management uses in
          evaluating segment results. The GAAP financial measure most directly
          comparable to total segment gross operating margin is operating
          income.
        
      
           
        
      
          We define total segment gross operating margin as operating income
          before: (1) depreciation, amortization and accretion expenses; (2)
          non-cash asset impairment charges; (3) operating lease expenses for
          which we do not have the payment obligation; (4) gains and losses
          related to sales of assets and investments; (5) gains and losses
          related to property damage insurance recoveries; and (6) general and
          administrative costs. Gross operating margin by segment is
          calculated by subtracting segment operating costs and expenses (net
          of the adjustments noted above) from segment revenues, with both
          segment totals before the elimination of intercompany transactions.
          In accordance with GAAP, intercompany accounts and transactions are
          eliminated in consolidation. Gross operating margin is exclusive of
          other income and expense transactions, income taxes, the cumulative
          effect of changes in accounting principles and extraordinary
          charges. Gross operating margin is presented on a 100 percent basis
          before any allocation of earnings to noncontrolling interests.
        
      
           
        
      
