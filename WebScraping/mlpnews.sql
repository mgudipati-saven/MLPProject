          We define distributable cash flow as net income or loss attributable
          to limited partners adjusted for: (1) the addition of depreciation,
          amortization and accretion expense; (2) the addition of cash
          distributions received from unconsolidated affiliates less equity
          earnings from unconsolidated affiliates; (3) the subtraction of
          sustaining capital expenditures; (4) the addition of losses or
          subtraction of gains related to sales of assets and investments and
          property damage insurance recoveries; (5) the addition of cash
          proceeds from sales of assets and investments and property damage
          insurance recoveries; (6) the addition of losses or subtraction of
          gains on the monetization of interest rate derivative instruments
          recorded in accumulated other comprehensive income (loss); and (7)
          the addition or subtraction of other miscellaneous amounts (as
          applicable) that affect net income or loss for the period.
        
      
           
        
      
          Sustaining capital expenditures are capital expenditures (as defined
          by GAAP) resulting from improvements to and major renewals of
          existing assets. Such expenditures serve to maintain existing
          operations but do not generate additional revenues.
        
      
           
        
      
          Our management compares the distributable cash flow we generate to
          the cash distributions we expect to pay our partners. Using this
          metric, management computes our distribution coverage ratio.
          Distributable cash flow is an important non-GAAP financial measure
          for our limited partners since it serves as an indicator of our
          success in providing a cash return on investment. Specifically, this
          financial measure indicates to investors whether or not we are
          generating cash flows at a level that can sustain or support an
          increase in our quarterly cash distributions. Distributable cash
          flow is also a quantitative standard used by the investment
          community with respect to publicly traded partnerships because the
          value of a partnership unit is, in part, measured by its yield,
          which is based on the amount of cash distributions a partnership can
          pay to a unitholder. The GAAP measure most directly comparable to
          distributable cash flow is net cash flows provided by operating
          activities.
        
      
        
        
           
        
        
           
        
        
        
        
           
        
        
           
        
        
        
        
           
        
        
           
        
        
        
      
