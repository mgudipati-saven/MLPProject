      Enterprise Products Partners L.P. (“Enterprise”) (NYSE: EPD) today
      announced its financial results for the three months and year ended
      December 31, 2012. For the year ended 2012, Enterprise reported records
      for each of the following: net income of $2.4 billion; earnings per unit
      of $2.71 on a fully diluted basis; gross operating margin of $4.4
      billion; and distributable cash flow of $4.1 billion, which included
      $1.2 billion of cash proceeds from sales of assets.
    

      “Enterprise had another record year in 2012,” stated Michael A. Creel,
      president and CEO of Enterprise. “We benefited from record volumes in
      our fee-based businesses attributable to production growth, primarily in
      the Eagle Ford and Haynesville shale plays, and from strong domestic and
      international demand for NGLs, particularly from the U.S. petrochemical
      industry and exports. In 2012, our integrated pipeline system
      transported a record 4.3 million barrels per day of NGLs, crude oil,
      refined products and petrochemicals and a record 14.5 trillion Btus per
      day of natural gas; while our NGL fractionators averaged a record
      659,000 barrels per day and our processing plants handled a record 4.4
      billion cubic feet per day of natural gas on a fee basis.”
    

      “We generated $4.1 billion of distributable cash flow and increased our
      cash distributions with respect to 2012 by 5.6 percent to $2.57 per
      unit. Enterprise has increased its cash distribution rate for each of
      the last 34 consecutive quarters, the longest period for any of the
      publicly traded energy partnerships, and in excess of 5 percent for each
      of the last eight years. In 2012, we retained approximately $1.9 billion
      of cash flow to reinvest in our growth projects and reduce our need to
      issue additional equity. Excluding $1.2 billion of proceeds from the
      sales of non-core assets, our distributable cash flow was $2.9 billion,
      providing 1.3 times coverage of the distributions paid with respect to
      2012,” said Creel.
    

      “In 2012, we successfully completed and began operations for major
      growth projects totaling $2.9 billion of investment. Most of these
      projects were completed on or under budget and on time or ahead of
      schedule. During the fourth quarter of 2012, we completed $1.2 billion
      of large projects including our sixth NGL fractionator at Mont Belvieu,
      Texas in October 2012 and the expansion of our natural gas and NGL
      pipeline systems serving the Eagle Ford shale in South Texas. Earlier
      this week, we began commissioning the third train at our natural gas
      processing plant near Yoakum, Texas serving Eagle Ford shale producers.
      We expect this train to begin commercial operations at the beginning of
      March. These projects are contributing new sources of gross operating
      margin and distributable cash flow, and we expect to see the full
      benefit of these assets as volumes increase over the next few years,”
      continued Creel.
    

      “Our commercial, engineering and operations teams are doing a great job
      developing growth and expansion opportunities to meet the needs of our
      energy producing and consuming customers. We have announced
      approximately $7.2 billion of major capital projects under construction
      that are scheduled to begin commercial operations from 2013 to 2015.
      Approximately $2.4 billion of these projects are expected to begin
      operations and start generating cash flow in 2013. The revenues
      associated with these projects are predominately fee-based and the
      larger projects have the additional assurance of demand revenues or
      minimum volume commitments,” stated Creel.
    

      “In 2013, we expect continuing volume and gross operating margin growth
      from our fee-based natural gas processing activities, NGL pipelines and
      fractionators and crude oil pipelines and storage facilities as these
      growth projects begin operations. We also expect our natural gas
      processing margins will be lower in 2013 than 2012 due to lower ethane
      prices, and that our equity NGL production will be lower as our natural
      gas processing business continues to transition to a more fee-based
      business. We expect the growth in our fee-based businesses will offset
      the potential decrease in gross operating margin from the commodity
      portion of our natural gas processing business and, moreover, will
      support our distribution growth in 2013,” said Creel.
    

