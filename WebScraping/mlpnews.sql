      to capture organizational and operational efficiencies while eliminating
      inherent conflicts of interest. We also lowered our cost of capital by
      eliminating our general partner’s incentive distribution rights. These
      actions improve the cash accretion associated with our growth capital
      projects for the benefit of our limited partners. We believe we are well
      capitalized and have good access to the capital markets to fund our
      capital program. We want to thank our debt and equity investors again
