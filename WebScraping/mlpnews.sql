      Review of Fourth Quarter 2012 Results
    

      Net income for the fourth quarter of 2012 was $617 million versus $726
      million for the fourth quarter of 2011. Net income attributable to
      limited partners for the fourth quarter of 2012 was $0.68 per unit on a
      fully diluted basis compared to $0.82 per unit on a fully diluted basis
      for the fourth quarter of 2011. Net income for the fourth quarter of
      2012 included losses aggregating $27 million, or a loss of $0.03 per
      unit on a fully diluted basis, for non-cash asset impairments and
      similar charges. Net income for the fourth quarter of 2011 included a
      net benefit of $108 million, or $0.12 per unit on a fully diluted basis,
      primarily due to gains from the sales of assets.
    

      On January 14, 2013, we announced that the Board of Directors of
      Enterprise’s general partner approved an increase in the partnership’s
      quarterly cash distribution rate with respect to the fourth quarter of
      2012 to $0.66 per unit, representing a 6.5 percent increase over the
      $0.62 per unit rate that was paid with respect to the fourth quarter of
      2011. Enterprise generated distributable cash flow of $886 million for
      the fourth quarter of 2012 compared to $1.4 billion for the fourth
      quarter of 2011. Distributable cash flow for the fourth quarter of 2012
      included $31 million of net proceeds from the sale of assets, while
      distributable cash flow for the fourth quarter of 2011 included $593
      million of cash proceeds from the sale of assets.
    

      Enterprise’s distributable cash flow for the fourth quarter of 2012
      provided 1.5 times coverage of the cash distributions that will be paid
      on February 7, 2013 to unitholders of record on January 31, 2013. The
      partnership retained approximately $308 million of cash flow for the
      fourth quarter of 2012, which is available to reinvest in growth capital
      projects, reduce debt and decrease our need to issue additional equity.
    

      “The fourth quarter of 2012 demonstrated the benefits of our investments
      in fee-based businesses serving the growing shale plays. Despite our
      natural gas processing and related NGL marketing business reporting a
      $66 million decrease in gross operating margin compared to the fourth
      quarter of 2011, primarily due to lower NGL prices and equity NGL
      production, Enterprise reported record gross operating margin of $1.2
      billion for the fourth quarter of 2012 driven by record NGL, crude oil,
      refined products and petrochemical pipeline volumes of 4.5 million
      barrels per day, record NGL fractionation volumes of 707,000 barrels per
      day and record fee-based natural gas processing volumes of 4.7 billion
      cubic feet per day. This was a $61 million increase in gross operating
      margin compared to the fourth quarter of 2011. Likewise, our
      distributable cash flow, excluding the proceeds from sale of assets, was
      also a record $855 million in the fourth quarter of 2012, a $39 million
      increase compared to the fourth quarter of 2011.
    

      Review of Segment Performance for the Fourth
      Quarter of 2012
    

      NGL Pipelines & Services – Gross operating margin for the NGL
      Pipelines & Services segment was $632 million for the fourth quarter of
      2012 compared to $635 million for the same quarter of 2011.
    

      Enterprise’s natural gas processing and related NGL marketing business
      generated gross operating margin of $330 million for the fourth quarter
      of 2012 compared to $396 million for the fourth quarter of 2011. This
      $66 million decrease was largely due to the effects of a decrease in NGL
      prices and natural gas processing margins as well as a decrease in
      equity NGL production from our Rocky Mountain, Permian Basin and East
      Texas natural gas processing plants. These decreases were partially
      offset by an increase in gross operating margin from higher fee-based
      processing volumes at our South Texas and Louisiana facilities. The
      South Texas facilities include two processing trains at our new Yoakum,
      Texas natural gas processing complex that began operations in mid-2012.
      Enterprise’s equity NGL production (the NGLs that the partnership earns
      title to as a result of providing processing services) decreased to 96
      MBPD for the fourth quarter of 2012 compared to 112 MBPD for the fourth
      quarter of 2011 largely due to a decrease in ethane volume extracted in
      the fourth quarter of 2012. Enterprise reported a 15 percent increase in
      fee-based processing volumes to a record 4.7 Bcfd for the fourth quarter
      of 2012 compared to 4.1 Bcfd for the fourth quarter of 2011.
    

      Gross operating margin from the partnership’s NGL pipelines and storage
      business increased $50 million to $220 million for the fourth quarter of
      2012 from $170 million for the fourth quarter of 2011. Enterprise’s
      South Texas NGL pipelines, which include the new Eagle Ford NGL pipeline
      completed in mid-2012, reported a $27 million increase in gross
      operating margin on higher volumes. Gross operating margin from our NGL
      export dock on the Houston Ship Channel and a related pipeline increased
      $8 million due to higher volumes during the fourth quarter of 2012. The
      remainder of the increase in gross operating margin for this business in
      the fourth quarter of 2012 was attributable to over twenty NGL pipelines
      and storage and terminal facilities. NGL pipeline volumes were a record
      2.5 million BPD for the fourth quarter of 2012, a 269 MBPD increase from
      the fourth quarter of 2011.
    

      Enterprise’s NGL fractionation business reported record gross operating
      margin of $82 million for the fourth quarter of 2012 compared to $69
      million reported for the same quarter of 2011. This increase was
      attributable to a $24 million increase in gross operating margin at the
      partnership’s Mont Belvieu fractionators on a 100 MBPD increase in
      volume, which more than offset an $11 million decline in gross operating
      margin from Norco due to lower volumes and NGL prices. Enterprise began
      operations at its sixth NGL fractionator at Mont Belvieu in October
      2012. Fractionation volumes for the fourth quarter of 2012 increased 15
      percent to a record 707 MBPD compared to the same quarter in 2011.
    

      Onshore Natural Gas Pipelines & Services – Enterprise’s
      Onshore Natural Gas Pipelines & Services segment reported an $11 million
      increase in gross operating margin for the fourth quarter of 2012 to a
      record $210 million from $199 million for the fourth quarter of 2011.
      Total onshore natural gas pipeline volumes were 13.4 TBtud in the fourth
      quarter of 2012 compared to 13.2 TBtud in the fourth quarter of 2011.
    

      Gross operating margin from the Texas Intrastate system increased $18
      million from the fourth quarter of 2011 on a 9 percent increase in
      pipeline volumes, including growing production from the Eagle Ford
      Shale. The Acadian Gas system reported a $14 million increase in gross
      operating margin as a result of a full quarter of operations from its
      Haynesville Extension pipeline, which began operations on November 1,
      2011. The Jonah, Haynesville, San Juan and Fairplay gathering systems
      reported an aggregate $12 million decrease in gross operating margin for
      the fourth quarter of 2012 compared to the fourth quarter of 2011 on an
      aggregate net decrease in volumes of 378 billion British thermal units
      per day (“BBtud”). Enterprise had a $5 million decrease in gross
      operating margin from our Mississippi natural gas storage facilities,
      which were sold in December 2011.
    

      Onshore Crude Oil Pipelines & Services – Gross operating
      margin from the partnership’s Onshore Crude Oil Pipelines & Services
      segment increased to $135 million for the fourth quarter of 2012 from
      $67 million for the fourth quarter of 2011. This increase in gross
      operating margin for the fourth quarter of 2012 was attributable to
      increased pipeline volumes, primarily on the Seaway pipeline and Eagle
      Ford production growth handled by our South Texas pipelines; improved
      results from our Cushing storage facility; and higher crude oil
      marketing volumes and margins. Total onshore crude oil pipeline volumes
      increased 32 percent to a record 897 MBPD for the fourth quarter of 2012
      from 680 MBPD for the fourth quarter of 2011.
    

      Offshore Pipelines & Services – Gross operating margin for
      the Offshore Pipelines & Services segment was $42 million for the fourth
      quarter of 2012 compared to $60 million for the same quarter of 2011.
    

      The Independence Hub platform and Trail pipeline reported a $20 million
      decrease in aggregate gross operating margin to $14 million for the
      fourth quarter of 2012 from $34 million for the fourth quarter of 2011
      attributable to lower demand fee revenues and lower volumes. The
      Independence Hub platform earned demand fee revenues of approximately
      $4.6 million per month over a 60-month period that began when it
      commenced operations in March 2007 until that period expired in March
      2012. Natural gas volumes on the Independence Trail pipeline were 313
      BBtud for the fourth quarter of 2012 compared to 429 BBtud reported for
      the fourth quarter of 2011. Total offshore natural gas pipeline volumes
      (including those for Independence Trail) decreased 275 BBtud to 786
      BBtud for the fourth quarter of 2012 compared to the fourth quarter of
      2011.
    

      Gross operating margin from Enterprise’s offshore crude oil pipeline
      business was $26 million for the fourth quarter of 2012 compared to $20
      million for the fourth quarter of 2011 primarily due to improved results
      from the Constitution, Poseidon and Cameron Highway pipelines. Total
      offshore crude oil pipeline volumes increased 54 MBPD to 336 MBPD in the
      fourth quarter of 2012 from 282 MBPD in the same quarter of 2011.
      Enterprise’s crude oil pipeline volumes for the fourth quarter of 2012
      were the highest since the federal government initiated its temporary
      drilling moratorium in June 2010.
    

      Petrochemical & Refined Products Services – Gross operating
      margin for the Petrochemical & Refined Products Services segment was
      $143 million in the fourth quarter of 2012 compared to $137 million in
      the fourth quarter of 2011.
    

      The partnership’s propylene business reported gross operating margin of
      $34 million for the fourth quarter of 2012 compared to $44 million in
      the fourth quarter of 2011 primarily due to lower volumes and sales
      margins and higher operating expenses. Propylene fractionation volumes
      were 69 MBPD in the fourth quarter of 2012 compared to 75 MBPD for the
      same quarter of 2011. Related propylene pipeline volumes were 116 MBPD
      during the fourth quarter of 2012 compared to 119 MBPD in the fourth
      quarter of 2011.
    

      Enterprise’s butane isomerization business reported gross operating
      margin of $25 million in the fourth quarter of 2012 compared to $32
      million in the fourth quarter of 2011 due to a decrease in isomerization
      volumes and lower revenues from the sales of by-products. Butane
      isomerization volumes during the fourth quarter of 2012 were 93 MBPD
      compared to 106 MBPD in the fourth quarter of 2011.
    

      Gross operating margin for Enterprise’s octane enhancement and
      high-purity isobutylene business was $13 million for the fourth quarter
      of 2012 compared to $27 million for the fourth quarter of 2011 largely
      due to lower volumes as our octane enhancement plant began its annual
      turnaround in December 2012, which has historically been done in the
      first quarter. The octane enhancement plant completed its turnaround and
      returned to full operations in mid-January 2013.
    

      Enterprise’s refined products pipelines and related services business
      reported gross operating margin of $53 million for the fourth quarter of
      2012 compared to $18 million for the fourth quarter of 2011. This $35
      million increase in gross operating margin was largely due to lower
      operating expenses and improved performance in refined products
      marketing, which more than offset a 25 MBPD decrease in pipeline
      volumes. Pipeline volumes for this business were 589 MBPD for the fourth
      quarter of 2012 compared to 614 MPBD for the fourth quarter of 2011.
    

      Enterprise’s marine transportation and other services business reported
      gross operating margin of $19 million for the fourth quarter of 2012
      compared to $17 million for the fourth quarter of 2011.
    

